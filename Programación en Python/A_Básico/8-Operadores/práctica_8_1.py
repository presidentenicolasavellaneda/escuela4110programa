# operadores
# los operadores siren para ejecutar operaciones
# sobre valores y variables

# Estos ejemplos abarcan los operadores aritméticos básicos 
# y cómo se pueden utilizar en Python para realizar diferentes tipos de cálculos. 
# Cada operador tiene su propia utilidad y contexto en el que es más adecuado usarlo

# operador suma
a = 10
b = 20
resultado_suma = a + b
print('el resultado de sumar a más b es :' , resultado_suma)

# operador resta
c = 30
d = 15
resultado_resta = c - d
print('El resultado de restar d de c es:', resultado_resta)

# operador multiplicación
e = 5
f = 6
resultado_multiplicacion = e * f
print('El resultado de multiplicar e por f es:', resultado_multiplicacion)

# operador división
g = 50
h = 5
resultado_division = g / h
print('El resultado de dividir g entre h es:', resultado_division)

# operador módulo o resto
i = 28
j = 10
resultado_modulo = i % j
print('El resultado del módulo de i por j es:', resultado_modulo)

# operador exponente
k = 2
l = 3
resultado_exponente = k ** l
print('El resultado de elevar k a la potencia de l es:', resultado_exponente)

# operador división entera
m = 20
n = 3
resultado_division_entera = m // n
print('El resultado de la división entera de m por n es:', resultado_division_entera)


