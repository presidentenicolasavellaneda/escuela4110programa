# operadores de comparación
# Los operadores de comparación se usan para comparar dos valores.

# Igual que (==): Comprueba si dos valores son iguales. Si lo son, devuelve True; si no, devuelve False.
# No es igual que (!=): Comprueba si dos valores son diferentes. Si lo son, devuelve True; si no, devuelve False.
# Mayor que (>): Comprueba si el valor de la izquierda es mayor que el de la derecha.
# Menor que (<): Comprueba si el valor de la izquierda es menor que el de la derecha.
# Mayor o igual que (>=): Comprueba si el valor de la izquierda es mayor o igual al de la derecha.
# Menor o igual que (<=): Comprueba si el valor de la izquierda es menor o igual al de la derecha.

# Igual que (==)
numero1 = 10
numero2 = 10
es_igual = numero1 == numero2
print('¿Es numero1 igual a numero2?', es_igual)  # Esto imprimirá: True

# No es igual que (!=)
numero3 = 15
numero4 = 20
no_es_igual = numero3 != numero4
print('¿Es numero3 diferente de numero4?', no_es_igual)  # Esto imprimirá: True

# Mayor que (>)
numero5 = 5
numero6 = 3
es_mayor = numero5 > numero6
print('¿Es numero5 mayor que numero6?', es_mayor)  # Esto imprimirá: True

# Menor que (<)
numero7 = 2
numero8 = 10
es_menor = numero7 < numero8
print('¿Es numero7 menor que numero8?', es_menor)  # Esto imprimirá: True

# Mayor o igual que (>=)
numero9 = 7
numero10 = 7
es_mayor_o_igual = numero9 >= numero10
print('¿Es numero9 mayor o igual que numero10?', es_mayor_o_igual)  # Esto imprimirá: True

# Menor o igual que (<=)
numero11 = 12
numero12 = 20
es_menor_o_igual = numero11 <= numero12
print('¿Es numero11 menor o igual que numero12?', es_menor_o_igual)  # Esto imprimirá: True

