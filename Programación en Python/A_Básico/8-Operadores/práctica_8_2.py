# operadores de asignación
# Los operadores de asignación se utilizan para guardar valores en variables.

# Asignación simple (=): Guarda un valor en una variable.
# Asignación con suma (+=): Añade un valor al valor actual de la variable.
# Asignación con resta (-=): Resta un valor del valor actual de la variable.
# Asignación con multiplicación (*=): Multiplica el valor actual de la variable por otro valor.
# Asignación con división (/=): Divide el valor actual de la variable por otro valor y guarda el resultado.
# Asignación con módulo (%=): Calcula el resto de la división del valor actual de la variable por otro valor y guarda el resto.
# Asignación con exponente (**=): Eleva el valor actual de la variable a la potencia de otro valor.
# Asignación con división entera (//=): Divide el valor actual de la variable por otro valor, pero guarda solamente la parte entera del resultado.

# Asignación simple
numero = 10
print('Valor inicial:', numero)

# Asignación con suma
# Esto es equivalente a numero = numero + 5
numero += 5
print('Después de añadir 5:', numero)

# Asignación con resta
# Esto es equivalente a numero = numero - 3
numero -= 3
print('Después de restar 3:', numero)

# Asignación con multiplicación
# Esto es equivalente a numero = numero * 2
numero *= 2
print('Después de multiplicar por 2:', numero)

# Asignación con división
# Esto es equivalente a numero = numero / 2
numero /= 2
print('Después de dividir por 2:', numero)

# Asignación con módulo
# Esto es equivalente a numero = numero % 4
numero %= 4
print('El resto después de dividir por 4:', numero)

# Asignación con exponente
# Esto es equivalente a numero = numero ** 2
numero **= 2
print('Después de elevar al cuadrado:', numero)

# Asignación con división entera
# Esto es equivalente a numero = numero // 3
numero //= 3
print('Después de la división entera por 3:', numero)
