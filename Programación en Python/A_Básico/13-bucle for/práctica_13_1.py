# Lista de frutas
frutas = ["manzana", "banana", "cereza", "durazno"]

# Bucle for para recorrer la lista de frutas
for fruta in frutas:
    print("Hoy tenemos:", fruta)

# Lista de Frutas: 
# Primero, creamos una lista llamada frutas que contiene los nombres de cuatro frutas diferentes.
# Bucle For: Utilizamos un bucle for para recorrer todos los elementos de la lista. 
# En cada iteración, la variable fruta toma el valor del elemento actual de la lista 
# (empezando por "manzana" y terminando en "durazno").
# Imprimir en Pantalla: En cada ciclo del bucle, 
# imprimimos un mensaje que dice "Hoy tenemos:" seguido por el nombre de la fruta 
# que está siendo procesada en esa iteración.