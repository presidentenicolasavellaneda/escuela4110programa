# Vamos a contar hasta 10 usando la función range()

print("Vamos a contar hasta 10:")

for numero in range(1, 11):  # Inicia en 1 y termina en 10
    print(numero)

print("¡Hemos terminado de contar!")

# Mensaje Inicial: 
# Primero, imprimimos un mensaje diciendo que vamos a contar hasta 10.
# Bucle For con Range: Iniciamos un bucle for que usa la función range(1, 11). 
# Esto significa que numero tomará valores desde 1 hasta 10 (11 no está incluido).
# Imprimir Número: 
# En cada iteración del bucle, imprimimos el valor de numero.
# Mensaje Final: Después de salir del bucle, imprimimos que hemos terminado de contar.