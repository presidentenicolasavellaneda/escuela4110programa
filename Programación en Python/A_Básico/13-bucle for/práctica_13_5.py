# Vamos a contar de 2 en 2 hasta 20

print("Vamos a contar de 2 en 2 hasta 20:")

for numero in range(2, 21, 2):  # Comienza en 2, termina en 20, incrementando de 2 en 2
    print(numero)

print("¡Hemos terminado de contar de dos en dos!")

# Mensaje Inicial: 
# Empezamos con un mensaje que indica que vamos a contar de dos en dos hasta 20.
# Bucle For con Range: 
# Usamos la función range(2, 21, 2). 
# Aquí el primer número (2) es el inicio de la secuencia, 
# el segundo número (21) es el límite superior que no se incluye en la secuencia, y el tercer número (2) indica el incremento, 
# es decir, de cuánto en cuánto se aumenta el número anterior.
# Imprimir Número: 
# Cada número generado por range() se imprime, mostrando la secuencia de contar de dos en dos.
# Mensaje Final: Después del bucle, imprimimos un mensaje que dice que hemos terminado de contar.