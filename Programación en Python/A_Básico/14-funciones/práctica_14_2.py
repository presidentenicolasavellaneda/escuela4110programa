# Concepto: Una función es como una receta que nos dice cómo hacer algo paso a paso. 
# Cada vez que queremos hacer eso, 
# simplemente seguimos la receta.

# Ahora, hagamos una función que puede tomar un nombre y decir hola a esa persona.

def saludar_persona(nombre):
    print("Hola, " + nombre + "!")

# Aquí, nombre es lo que llamamos un parámetro, 
# que es como una variable que usamos dentro de la función 
# para hacer algo más personalizado.

saludar_persona("Ana")  # Esto imprimirá: Hola, Ana!