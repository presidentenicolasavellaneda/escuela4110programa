# Inicializamos el contador
contador = 1

# Configuramos el bucle while para ejecutarse mientras el contador sea menor o igual a 5
while contador <= 5:
    print("El contador está en:", contador)
    # Incrementamos el contador en 1 en cada iteración
    contador += 1

print("¡Hemos terminado de contar!")

# Inicialización del contador: Comenzamos con contador = 1, 
# estableciendo el punto de partida del contador.
# Condiciones del bucle while: La condición while contador <= 5 mantiene el bucle en ejecución 
# siempre y cuando el valor de contador sea 5 o menos.
# Imprimir el valor del contador: En cada iteración del bucle, 
# imprimimos el valor actual del contador.
# Incrementar el contador: Dentro del bucle, incrementamos el valor de contador en 1 (contador += 1) 
# después de imprimir su valor actual. Esto asegura que el contador eventualmente superará el valor 5, 
# y la condición del bucle se volverá falsa, terminando el bucle.
# Mensaje final: Una vez que el bucle se ha completado, 
# se imprime un mensaje que indica que la cuenta ha terminado.