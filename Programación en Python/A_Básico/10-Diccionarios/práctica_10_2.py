# Crear y usar un diccionario en Python
# Imagina que tienes diferentes frutas y quieres saber cuántos tipos de frutas tienes en total.

# Diccionario de frutas con la cantidad que tienes
inventario_de_frutas = {
    'manzana': 10,
    'banana': 5,
    'cereza': 20,
    'naranja': 8,
    'kiwi': 3
}

# Mostrar el diccionario completo
print('Inventario completo de frutas:', inventario_de_frutas)

# Obtener la longitud del diccionario
# Esto te dirá cuántos tipos diferentes de frutas tienes.
cantidad_de_tipos_de_frutas = len(inventario_de_frutas)
print('Cantidad de tipos diferentes de frutas en el inventario:', cantidad_de_tipos_de_frutas)

# Crear el diccionario: Se inicia con la creación de un diccionario llamado inventario_de_frutas, 
# que contiene varios tipos de frutas y la cantidad de cada una.

# Mostrar el diccionario: Es bueno mostrar todo el diccionario para que vean qué datos contiene.

# Usar len() para obtener la longitud: La función len() se usa para contar cuántas claves hay en el diccionario, 
# lo cual corresponde a los diferentes tipos de frutas en el inventario.