# Usar clear() para limpiar listas en Python
# Imagina que tienes una caja llena de frutas, pero decides que quieres empezar de nuevo y vaciarla.

# Lista de frutas
frutas = ['manzana', 'banana', 'cereza', 'naranja', 'kiwi']

# Mostrar la lista original
print('Lista original de frutas:', frutas)

# Usar el método clear() para vaciar la lista
frutas.clear()

# Mostrar la lista después de usar clear()
print('Lista de frutas después de usar clear():', frutas)

# Lista original: Se muestra la lista inicial de frutas

# Usar clear(): El método clear() se utiliza para remover todos los elementos de la lista, 
# dejándola vacía. Es como vaciar una caja de juguetes o borrar un pizarrón.

# Mostrar la lista vacía: Después de limpiar la lista, se muestra de nuevo para confirmar 
# que todos los elementos han sido eliminados y la lista está ahora vacía.