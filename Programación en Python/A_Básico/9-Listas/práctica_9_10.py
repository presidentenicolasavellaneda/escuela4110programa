# Usar reverse() para invertir el orden de los elementos en una lista
# Imagina que tienes una lista de frutas y quieres ver cómo se vería si las ordenaras al revés.

# Lista de frutas
frutas = ['manzana', 'banana', 'cereza', 'naranja', 'kiwi']

# Mostrar la lista original de frutas
print('Lista original de frutas:', frutas)

# Usar reverse para invertir el orden de las frutas en la lista
frutas.reverse()
print('Lista de frutas después de invertir el orden:', frutas)

