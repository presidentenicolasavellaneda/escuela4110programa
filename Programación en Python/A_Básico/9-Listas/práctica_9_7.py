# Usar count() para contar elementos en una lista
# Imagina que quieres saber cuántas manzanas hay en tu canasta de frutas.

# Lista de frutas con algunas repetidas
frutas = ['manzana', 'banana', 'cereza', 'naranja', 'kiwi', 'manzana', 'banana', 'manzana']

# Mostrar la lista de frutas
print('Lista de frutas:', frutas)

# Contar cuántas veces aparece la manzana
cantidad_manzanas = frutas.count('manzana')
print('Número de manzanas en la lista:', cantidad_manzanas)

# Contar cuántas veces aparece la cereza
cantidad_cerezas = frutas.count('cereza')
print('Número de cerezas en la lista:', cantidad_cerezas)
